// common array method

let color = ["red", "blue", "yellow"]
color.push("green")  //add an element to the end of array
console.log(color);

color.pop()     //remove the last element of the array
console.log(color);

color.shift();  //remove the first element of the array
console.log(color);

color.unshift("purple") //add elements to the begining of the array
console.log(color);
