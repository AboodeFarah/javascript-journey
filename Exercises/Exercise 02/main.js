//Declare two variables a with a value of 15 and b with a value of "15". Use all comparison operators and log the results.

let a = 15;
let b = "15";

//equal to
console.log(a == b); //true

// //not equal to 
console.log(a != b); //false 

// // Strictly equal to 
console.log(a === b); //false 

// // Strictly  not equal to
console.log(a !== b); //true

// //greater than
console.log(a > b); //false

// //less than
console.log(a < b); //false

// // Greater than or equal to 
console.log(a >= b); //true

// // less than or equal to 
console.log(a <= b); //true