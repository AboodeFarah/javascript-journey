
let x = 10 ;
let y = "10" ;

// equal to 
console.log(x == y)   //true

// not equal to
console.log(x != y)  //false

// strictly equal to 
console.log(x === y);   //false 

// strictly not equal to 
console.log(x !== y);   //true

//greater than
console.log(x > y);

//less than
console.log(x < y);

//greater than or equal
console.log(x >= y);

// less than or equal
console.log(x <= y);
